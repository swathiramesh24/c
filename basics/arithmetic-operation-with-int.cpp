#include<iostream>
using namespace std;

int main()
{
    int num1=12;  //int variable declaration
    int num2=6;
    
    int add_result=num1+num2; //adds both numbers
    int subtract_result=num1-num2; //subtracts both numbers
    int multiply_result=num1*num2; //multiplies both numbers
    int divide_result=num1/num2; //quotient of both numbers
    int modulo_result=num1%num2; //remainder of both numbers
    
    cout<< add_result <<endl;
    cout<< subtract_result <<endl;
    cout<< multiply_result <<endl;
    cout<< divide_result <<endl;
    cout<< modulo_result <<endl;
    
    return 0;
}
